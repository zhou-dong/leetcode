package org.dzhou.other.miscellaneous.gumgum;

public interface ReverseNumberGenerator {

	/**
	 * This method reverses a given number. For example if you give
	 * 1234, it will return 4321. If a single digit number is passed,
	 * this method will return the same number.
	 */
	Integer reverseANumber(Integer number);
}
